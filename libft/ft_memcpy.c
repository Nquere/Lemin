#include <string.h>

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	char	*a;
	char	*b;

	a = (char*)s1;
	b = (char*)s2;
	while (n--)
	{
		*a = *b;
		a++;
		b++;
	}
	return (s1);
}
